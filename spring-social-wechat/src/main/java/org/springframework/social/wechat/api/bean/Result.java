package org.springframework.social.wechat.api.bean;

public class Result {

	private String code = null;

	private String msg = null;

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

}